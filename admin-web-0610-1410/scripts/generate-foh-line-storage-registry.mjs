/**
 * 从前厅相关 module-settings UI 文件抽取 seq → lines localStorage 键
 * 输出：src/config/foh-settings-line-storage-registry.ts
 */
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";
import { extractLineStorageMap } from "./lib/foh-line-scope-extract.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUT_PATH = path.resolve(__dirname, "../src/config/foh-settings-line-storage-registry.ts");

const entries = [...extractLineStorageMap().entries()].sort((a, b) => a[0] - b[0]);
const ts = `/** AUTO-GENERATED by scripts/generate-foh-line-storage-registry.mjs — do not edit */

/** seq → localStorage JSON 键（适用产线多选） */
export const FOH_LINE_STORAGE_BY_SEQ: Record<number, string> = {
${entries.map(([seq, id]) => `  ${seq}: ${JSON.stringify(id)},`).join("\n")}
};

export function hasFohLineStorage(seq: number): boolean {
  return Object.prototype.hasOwnProperty.call(FOH_LINE_STORAGE_BY_SEQ, seq);
}
`;

fs.writeFileSync(OUT_PATH, ts, "utf8");
console.log(`Wrote ${OUT_PATH} (${entries.length} seqs)`);
